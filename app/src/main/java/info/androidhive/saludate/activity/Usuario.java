public class Usuario {
    private String nombre;
    private String Apellidos;
    private int edad;
    private String sexo;
    private int dni;
    private String password;

    public String getNombre() {
        return nombre;
    }

    public String getApellidos() {
        return Apellidos;
    }

    public int getEdad() {
        return edad;
    }

    public String getSexo() {
        return sexo;
    }

    public int getDni() {
        return dni;
    }

    public String getPassword() {
        return password;
    }
}
